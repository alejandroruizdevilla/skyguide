  // Current language
  currentLanguage: 'en',

  // Initialize the translation system
  init: function() {
    this.currentLanguage = localStorage.getItem('skyguide-language') || 'en';
    this.applyTranslations();
    this.updateHtmlLang();
  },

  // Get a translation by key
  get: function(key) {
    const langData = this.translations[this.currentLanguage] || this.translations['en'];
    return langData[key] || this.translations['en'][key] || key;
  },

  // Set the current language
  setLanguage: function(lang) {
    if (this.translations[lang]) {
      this.currentLanguage = lang;
      localStorage.setItem('skyguide-language', lang);
      this.applyTranslations();
      this.updateHtmlLang();
    }
  },

  // Update HTML lang attribute
  updateHtmlLang: function() {
    document.documentElement.lang = this.currentLanguage;
  },

  // Apply translations to all elements with data-i18n attribute
  applyTranslations: function() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translation = this.get(key);
      if (translation && translation !== key) {
        el.textContent = translation;
      }
    });

    // Also update placeholder attributes
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const translation = this.get(key);
      if (translation && translation !== key) {
        el.placeholder = translation;
      }
    });

    // Update title attributes
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      const translation = this.get(key);
      if (translation && translation !== key) {
        el.title = translation;
      }
    });

    // Dispatch event for custom handlers
    window.dispatchEvent(new CustomEvent('languageChanged', {
      detail: { language: this.currentLanguage }
    }));
  },

